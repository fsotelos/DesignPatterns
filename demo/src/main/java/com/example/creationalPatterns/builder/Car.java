package com.example.creationalPatterns.builder;

public class Car {

    public String make;
    public String model;
    public String color;
    public int year;
    public int horsePower;

    public Car(){

    }
    public String getMake() {
        return make;
    }

    public String getModel() {
        return model;
    }

    public int getYear() {
        return year;
    }

    public String getColor() {
        return color;
    }

    public int getHorsepower() {
        return horsePower;
    }
} 
